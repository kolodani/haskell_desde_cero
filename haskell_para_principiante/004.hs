-- suma todos los valores de una lista, la funcion que se usa es la sum
lista1 = [1, 2, 3, 4, 5, 6]

sumalista1 = sum lista1

-- el producto de todos los valores de una lista, la funcion que se usa es product
producto1 = product lista1

-- ver si un elemento existe dentro de una lista, la funcion que se usa es elem
existe1 = 5 `elem` lista1